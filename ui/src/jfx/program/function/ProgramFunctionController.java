package jfx.program.function;

import javafx.event.ActionEvent;
import javafx.fxml.FXML;
import javafx.scene.control.Button;
import javafx.scene.control.Label;

public class ProgramFunctionController {

    @FXML
    private Button collapseButton;

    @FXML
    private Button collapseButton1;

    @FXML
    private Label degreeInfoLabel;

    @FXML
    private Label degreeInfoLabel1;

    @FXML
    private Button expandButton;

    @FXML
    void handleCollapse(ActionEvent event) {
        event.consume();
        collapseButton.setDisable(true);
    }

    @FXML
    void handleExpand(ActionEvent event) {
        event.consume();
        expandButton.setDisable(true);
    }

}
